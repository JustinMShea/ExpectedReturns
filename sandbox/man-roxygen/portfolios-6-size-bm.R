#' @section Six Size/Book-to-market Portfolios:
#'
#' The portfolios, which are constructed at the end of each June, are the intersections
#' of 2 portfolios formed on size (\eqn{ME}) and 3 portfolios formed on the \eqn{BM}.
#'
#' The *size breakpoints* for year \eqn{t} is the median NYSE market equity at the
#' end of June of year \eqn{t} (see [K. R. French's Detail for ME Breakpoints](https://mba.tuck.dartmouth.edu/pages/faculty/ken.french/Data_Library/det_me_breakpoints.html)).
#'
#' The \eqn{BM} for June of year \eqn{t} is the book equity for the last fiscal
#' year end in \eqn{t-1} divided by \eqn{ME} for December of \eqn{t-1}.
#' The *BM breakpoints* are the 30th and 70th NYSE percentiles (see [K. R. French's Detail for BM Breakpoints](http://mba.tuck.dartmouth.edu/pages/faculty/ken.french/Data_Library/det_beme_breakpoints.html)).
