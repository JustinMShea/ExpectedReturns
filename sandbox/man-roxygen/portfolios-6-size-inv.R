#' @section Six size/investment portfolios:
#'
#' These portfolios are constructed at the end of each June and represent the intersections
#' of 2 portfolios formed on market equity, denoted \eqn{ME}, and 3 portfolios formed
#' on investment (\eqn{INV}).
#' The *size breakpoint* for year \eqn{t} is the median NYSE market equity at the end
#' of June of year \eqn{t} (see [K. R. French's Detail for ME Breakpoints](http://mba.tuck.dartmouth.edu/pages/faculty/ken.french/Data_Library/det_inv_breakpoints.html)).
#' *Investment* is the change in total assets from the fiscal year ending in year \eqn{t-2}
#' to the fiscal year ending in \eqn{t-1}, divided by \eqn{t-2} total assets.
#' The *investment ratio breakpoints* are the 30th and 70th NYSE percentiles (see [K. R. French's Detail for Investment Breakpoints](http://mba.tuck.dartmouth.edu/pages/faculty/ken.french/Data_Library/det_inv_breakpoints.html)).
